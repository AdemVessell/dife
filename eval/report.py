"""Generate RESULTS.md summarising all benchmark outcomes."""

import csv
import os


def write_results_md(results_dir: str) -> None:
    """Read all summary.csv files and write RESULTS.md at results_dir/../RESULTS.md.

    Args:
        results_dir: directory containing per-benchmark subdirectories
    """
    bench_names = []
    for entry in os.scandir(results_dir):
        if entry.is_dir() and os.path.exists(os.path.join(entry.path, "summary.csv")):
            bench_names.append(entry.name)
    bench_names.sort()

    lines = ["# Evaluation Results\n"]
    lines.append(
        "Auto-generated by `run_all.py`. "
        "Each table shows mean ± std across seeds.\n"
    )

    for bench in bench_names:
        csv_path = os.path.join(results_dir, bench, "summary.csv")
        with open(csv_path, newline="") as f:
            reader = csv.DictReader(f)
            rows = list(reader)

        if not rows:
            continue

        lines.append(f"\n## {bench}\n")

        # Build markdown table
        headers = ["Method", "AA ↑", "AF ↓", "BWT ↑", "FWT ↑", "Replay Budget"]
        col_keys = [
            ("AA_mean", "AA_std"),
            ("AF_mean", "AF_std"),
            ("BWT_mean", "BWT_std"),
            ("FWT_mean", "FWT_std"),
            ("replay_budget_mean", "replay_budget_std"),
        ]
        lines.append("| " + " | ".join(headers) + " |")
        lines.append("| " + " | ".join(["---"] * len(headers)) + " |")

        for row in rows:
            cells = [row["method"]]
            for mean_k, std_k in col_keys:
                mean_val = float(row.get(mean_k, 0))
                std_val = float(row.get(std_k, 0))
                if "replay" in mean_k:
                    cells.append(f"{mean_val:.0f} ± {std_val:.0f}")
                else:
                    cells.append(f"{mean_val:.3f} ± {std_val:.3f}")
            lines.append("| " + " | ".join(cells) + " |")

        # Note plots
        plot_dir = os.path.join(bench, "plots")
        lines.append(f"\nPlots: `results/{plot_dir}/`\n")

    md_path = os.path.join(results_dir, "..", "RESULTS.md")
    md_path = os.path.normpath(md_path)
    with open(md_path, "w") as f:
        f.write("\n".join(lines) + "\n")
    print(f"Wrote {md_path}")
